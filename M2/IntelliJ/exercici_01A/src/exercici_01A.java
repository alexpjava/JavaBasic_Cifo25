public class exercici_01A {

    public static float suma (float num1, float num2){
        //float resultat;
        float resultat = num1 + num2;

        return resultat;
    }
    public static float resta (float num1, float num2){
        //float resultat;
        float resultat = num1 - num2;

        return resultat;
    }

    public static float multiplicacio (float num1, float num2){
        //float resultat;
        float resultat = num1 * num2;

        return resultat;
    }

    public static float divisio (float num1, float num2){
        //float resultat;
        float resultat = num1 / num2;

        return resultat;
    }

}
